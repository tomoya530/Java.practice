
public class Chapter07 {
	public static void main(String[] args) {
		int dis = 50;
		if (dis <= 5) {
			System.out.println("とても近いです");
		} else if (dis <= 10) {
			System.out.println("近いです");
		} else if (dis <= 15) {
			System.out.println("遠いです");
		} else {
			System.out.println("とても遠いです");
		}
	}
}
